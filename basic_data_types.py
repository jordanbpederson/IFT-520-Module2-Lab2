# Demonstrating five simple data types
# Integer (like whole ingredients)

integer_value = 10
print(f"Integer: {integer_value}")

# Float (like ingredients measured in parts)

float_value = 3.14

print(f"Float: {float_value}")

# Character (like a single item)
char_value = 'A'
print(f"Character: {char_value}")

# String (like a recipe text)

string_value = "Hello, world!"
print(f"String: {string_value}")

# Boolean (like a yes/no decision)

boolean_value = True
print(f"Boolean: {boolean_value}")